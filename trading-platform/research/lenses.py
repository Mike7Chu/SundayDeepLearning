def lens_by_key(key: str) -> Lens | None:
    return next((l for l in LENSES if l.key == key), None)


def lenses_block() -> str:
    """system 프롬프트에 넣을 거장 렌즈 설명 블록."""
    parts = []
    for l in LENSES:
        items = "\n".join(f"  - {c}" for c in l.checklist)
        parts.append(f"[{l.name}] {l.focus}\n{items}")
    return "\n\n".join(parts)


